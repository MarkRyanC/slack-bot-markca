class num():

    def add(self,num1,num2):
        res = num1 + num2
        print("The sum of %s and %s is %s" % (num1,num2,res))
        return res

    def divide(self,num1,num2):
        res = num1 / num2
        print("The result of %s divided by %s is %s" % (num1, num2, res))
        return res